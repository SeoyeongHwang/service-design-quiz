import { readdir, mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { pathToFileURL } from "node:url";
import { questions as existingQuestions } from "../src/data/questions.js";

const batchesDir = path.resolve("drafts/batches");
const compiledDir = path.resolve("drafts/compiled");
const outputPath = path.join(compiledDir, "questions.generated.js");

function serialize(value) {
  return JSON.stringify(value, null, 2)
    .replace(/"([^"]+)":/g, "$1:")
    .replaceAll("\"", "\"");
}

const entries = await readdir(batchesDir, { withFileTypes: true });
const batchFiles = entries
  .filter(
    (entry) =>
      entry.isFile() &&
      entry.name.endsWith(".js") &&
      entry.name !== "README.md",
  )
  .map((entry) => path.join(batchesDir, entry.name))
  .sort((left, right) => left.localeCompare(right));

if (!batchFiles.length) {
  console.log("No batch files found in drafts/batches.");
  process.exit(0);
}

const allQuestions = [...existingQuestions];
const seenIds = new Set(existingQuestions.map((question) => question.id));
const loadedBatches = [];

for (const filePath of batchFiles) {
  const moduleUrl = pathToFileURL(filePath).href;
  const { batchMeta, batchQuestions } = await import(moduleUrl);

  if (!batchMeta || !Array.isArray(batchQuestions)) {
    throw new Error(`Invalid batch file: ${filePath}`);
  }

  for (const question of batchQuestions) {
    if (seenIds.has(question.id)) {
      throw new Error(`Duplicate question id detected during merge: ${question.id}`);
    }

    seenIds.add(question.id);
    allQuestions.push(question);
  }

  loadedBatches.push({
    batchId: batchMeta.batchId,
    section: batchMeta.section,
    count: batchQuestions.length,
  });
}

await mkdir(compiledDir, { recursive: true });

const fileContents = `// Generated by scripts/merge-question-batches.mjs
// Review this file before copying the merged result into src/data/questions.js.

export const mergedBatchSummary = ${serialize(loadedBatches)};

export const mergedQuestions = ${serialize(allQuestions)};
`;

await writeFile(outputPath, fileContents, "utf8");

console.log(`Merged ${loadedBatches.length} batch files into ${path.relative(process.cwd(), outputPath)}.`);
